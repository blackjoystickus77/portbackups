--Kindly Borrowed from the B3313 port to change area names on the player list because that little detail going untouched ticked me off.

areas = {
    [LEVEL_CASTLE_GROUNDS] = "Spiral Mountain",
    [LEVEL_CASTLE] = "Gruntilda's Lair",
}

local function area_name_update() --Updates Playerlist
    for i = 0, MAX_PLAYERS - 1 do
        local m = gMarioStates[i]
        local np = gNetworkPlayers[i]
        local defaultAreaName = get_level_name(gNetworkPlayers[m.playerIndex].currCourseNum, gNetworkPlayers[m.playerIndex].currLevelNum, gNetworkPlayers[m.playerIndex].currAreaIndex)

        if not hide_names then
            -- Check if the current level has a custom name defined in our 'areas' table
            if areas[gNetworkPlayers[m.playerIndex].currLevelNum] ~= nil then
                gPlayerSyncTable[m.playerIndex].location = areas[gNetworkPlayers[m.playerIndex].currLevelNum]
            else
                gPlayerSyncTable[m.playerIndex].location = defaultAreaName
            end
        else
            gPlayerSyncTable[m.playerIndex].location = " "
        end
        network_player_set_override_location(np, gPlayerSyncTable[m.playerIndex].location)
    end
end

hook_event(HOOK_UPDATE, area_name_update)